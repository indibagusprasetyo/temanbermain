# ELSE IF (ELIF)

nama = input("Nama Kamu Siapa???")

if nama == "ucup": #kondisi 1
    print("Hai UCUP!!!") # aksi 1 
elif nama == "otong": #kondisi 2
    print("Hai OTONGGG!!!!") # aksi 2
elif nama == "bagus": # kondisi 3
    print("Hello WORLDDDD") # aksi 3
else:
    print("Maaf anda siapa ya?") # aksi false

print("Ini adalah akhir program")