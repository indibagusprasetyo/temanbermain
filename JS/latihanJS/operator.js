// Operator

let x = 10;
let y = 2;
let z = 12;

//contoh alternatif penambahan
x += y;
x += y -= z;

console.log(x);
console.log(z);

//contoh lainnya
x += z //artinya x = x + z
x -= y //artinya x = x - y
x *= z //artinya x = x * z
x /= y //artinya x = x / y
x %= z //artinya x = x % z